# Number log() method - returns the natural logarithm of x, for x > 0
import math # This will import math module
print('math.log(99.12) : ', math.log(99.12))
print('math.log(100.72) : ', math.log(100.72))
print('math.log(math.pi) : ', math.log(math.pi))

#Number log10() method - Returns base-10 logarithm of x for x > 0
print('math.log10(99.12) : ', math.log10(99.12))
print('math.log10(67.72) : ', math.log10(67.72))
print('math.log10(989) : ', math.log10(989))
print('math.log10(math.pi) : ', math.log10(math.pi))
