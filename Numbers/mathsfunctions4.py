#Number round() method - built-in function in python, returns x rounded to n digits from the decimal point.
print ("round(70.23456) : ", round(70.23456))
print ("round(56.659,1) : ", round(56.659,1))
print ("round(80.264, 2) : ", round(80.264, 2))
print ("round(100.000056, 3) : ", round(100.000056, 3))
print ("round(-100.000056, 3) : ", round(-100.000056, 3))
print ("round(-100.000076, 4) : ", round(-100.000076, 4))
#Number sqrt() Method
import math
print('math.sqrt(144) : ', math.sqrt(144))
print('math.sqrt(6) : ', math.sqrt(6))
print('math.sqrt(math.pi) : ', math.sqrt(math.pi))