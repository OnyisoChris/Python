#Number modf() method - Returns the fractional and integer parts of x in a two-item tuple.
import math
print('math.modf(89.12) : ', math.modf(89.12))
print('math.modf(67.71) : ', math.modf(67.71))
print('math.modf(279) : ', math.modf(279))
print('math.modf(math.pi) : ', math.modf(math.pi))
